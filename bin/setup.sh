#!/bin/bash

WGET="/usr/bin/wget"
UNZIP="/usr/bin/unzip"
MV="/usr/bin/mv -v"
RM="/usr/bin/rm -v"

DOWNLOAD_PATH=/tmp
OUTPUT_PATH=./bin/Sia-linux-amd64

SIA_DAEMON_RELEASE=Sia-v1.4.4-linux-amd64
SIA_DAEMON_RELEASE_FILE=$SIA_DAEMON_RELEASE.zip
SIA_DAEMON_URL=https://sia.tech/releases
SIA_DAEMON_PATH_URL=$SIA_DAEMON_URL/$SIA_DAEMON_RELEASE_FILE
SIA_DAEMON_RELEASE_DOWNLOAD_PATH=$DOWNLOAD_PATH/$SIA_DAEMON_RELEASE_FILE

$WGET $SIA_DAEMON_PATH_URL -P $DOWNLOAD_PATH
$UNZIP $SIA_DAEMON_RELEASE_DOWNLOAD_PATH -d $DOWNLOAD_PATH
$MV $DOWNLOAD_PATH/$SIA_DAEMON_RELEASE ./bin/Sia-Network

$RM $SIA_DAEMON_RELEASE_DOWNLOAD_PATH